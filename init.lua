-- ====<[ init.lua: config ]>=====================================================
-- WARNING:
-- don't forget to wait for plugins & mason
-- to be loaded when using this config for the first time
vim.cmd("set splitright")
vim.cmd("set splitbelow")

vim.opt_local.expandtab = true
vim.opt_local.shiftwidth = 2
vim.opt_local.tabstop = 2
vim.opt_local.softtabstop = 2

vim.cmd("set number")
vim.cmd("set relativenumber")        -- Enable relative line numbers
vim.cmd("set clipboard=unnamedplus") -- Use the system clipboard
vim.cmd("set autoindent")            -- Enable automatic indentation

-- INFO::> enable cursor line
vim.cmd("set cursorline") -- Enable the cursor line

-- INFO::> navigations key maps
vim.keymap.set('n', 'c-k', ":wincmd k<CR>")
vim.keymap.set('n', 'c-j', ":wincmd j<CR>")
vim.keymap.set('n', 'c-l', ":wincmd l<CR>")
vim.keymap.set('n', 'c-h', ":wincmd h<CR>")

-- INFO::> lazy plugin manager
local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not (vim.uv or vim.loop).fs_stat(lazypath) then
  local lazyrepo = "https://github.com/folke/lazy.nvim.git"
  local out = vim.fn.system({
    "git",
    "clone",
    "--filter=blob:none",
    "--branch=stable",
    lazyrepo,
    lazypath
  })
  if vim.v.shell_error ~= 0 then
    vim.api.nvim_echo({
      { "Failed to clone lazy.nvim:\n", "ErrorMsg" },
      { out,                            "WarningMsg" },
      { "\nPress any key to exit..." },
    }, true, {})
    vim.fn.getchar()
    os.exit(1)
  end
end
vim.opt.rtp:prepend(lazypath)
-- Make sure to setup `mapleader` and `maplocalleader` before
-- loading lazy.nvim so that mappings are correct.
-- This is also a good place to setup other settings (vim.opt)
vim.g.mapleader = " "
vim.g.maplocalleader = "\\"
-- INFO: Lazy.nvim configuration with a border around floating windows
require('lazy').setup('plugins', {
  ui = {
    border = "rounded", -- Border style: "rounded", "solid", "double", "single", or "none"
    winblend = 0,       -- No transparency
  },
})

-- INFO:> custom key maps
vim.api.nvim_set_keymap("v", "<C-c>", '"+y', { noremap = true, silent = true })

-- TIP::> make neovim inherite transparency from iterm
vim.cmd [[highlight Normal guibg=NONE]]
vim.cmd [[highlight NormalNC guibg=NONE]]
vim.cmd [[highlight VertSplit guibg=NONE]]
vim.cmd [[highlight StatusLine guibg=NONE]]
vim.cmd [[highlight StatusLineNC guibg=NONE]]

vim.opt.termguicolors = true
